//boy's covered distance
#include<stdio.h>
#include<math.h>
int main()
{
    float area,cirumference,r,radius;
    area = 32000;
    area = 3.14*r*r;
    radius = sqrt(32000/3.14);
    cirumference = 2*3.14*radius/1000;
    printf("The total distance covered by boy is %f",5*cirumference);
    return 0;
}




    

